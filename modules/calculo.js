export function adcao(numberOne, numberTwo) {
  let result = numberOne + numberTwo

  alert(`Resultado da adcao é ${result}`) 
  return result
}

export function multiplicacao(numberOne, numberTwo) {
  let result = numberOne * numberTwo

  alert(`Resulçtado da divisão é ${result}`)
  return result
}

export function divisao(numberOne, numberTwo) {
  let result = numberOne / numberTwo
  let resto = numberOne % numberTwo

  alert(`Resultado da divisão ${result} e, o resto da divisão é ${resto}` )
  return result
}

export function subtracao(numberOne, numberTwo) {
  let result = numberOne - numberTwo

  alert(`Resultado da multiplicacao ${result}`)
  return result
}

